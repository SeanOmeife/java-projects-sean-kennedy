public class RelationalTest {
    public static void main(String[] args) {
        int x = 3, y = 4;
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x > y);
        System.out.println(x >= y);
        System.out.println(x < y);
        System.out.println(x <= y);
        System.out.println( 3 + 4.0);
        System.out.println( 4 == 4.0);
    }
}
